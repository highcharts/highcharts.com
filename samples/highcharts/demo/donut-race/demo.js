const data = TimeSeries.Security
    .map(security => security.HistoryDetail)
    .map(historyDetail =>
        historyDetail.map(detail => [detail.EndDate, +detail.Value])
    );

const startDay = new Date('2020-01-01'),
    endDay = new Date('2020-12-31'),
    btn = document.getElementById('play-pause-button'),
    input = document.getElementById('play-range'),
    names = ['AAPL', 'INTC', 'NVDA', 'AMD', 'MSFT'];

let currentDay = startDay;

function getData(date) {
    const dateString = date.toISOString().split('T')[0];
    return data.map((asset, i) => {
        const price = asset.filter(d => d[0] === dateString)[0]?.[1];
        if (!price) {
            return false;
        }
        return [names[i], price];
    });
}

function getSubtitle() {
    const day = String(currentDay.getDate()).padStart(2, '0'),
        month = String(currentDay.getMonth() + 1).padStart(2, '0'),
        year = currentDay.getFullYear();

    return `${day}.${month}.${year}`;
}

